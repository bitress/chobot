def seed_islands(db):
    """
    Seed the database with island data
    Only seeds if the islands table is empty
    
    Args:
        db: Database session
    """
    from admin.models import Island
    
    # Check if database already has islands
    existing_count = db.query(Island).count()
    
    if existing_count > 0:
        print(f"Database already contains {existing_count} islands. Skipping seed.")
        return
    
    # Insert all seed data
    for island_data in ISLAND_SEED_DATA:
        island = Island(**island_data)
        db.add(island)
    
    db.commit()
    print(f"Successfully seeded {len(ISLAND_SEED_DATA)} islands into the database.")
